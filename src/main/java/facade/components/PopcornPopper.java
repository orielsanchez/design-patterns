package facade.components;

public class PopcornPopper {

    public void on() {
        System.out.println("Popcorn Popper is on");
    }

    public void pop() {
        System.out.println("Popcorn Popper is popping");
    }

    public void off() {
        System.out.println("Popcorn Popper is off");
    }
}
